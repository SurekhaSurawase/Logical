package Logical_programs;

public class Swapping2numUsing3rdnum {

	public static void main(String[] args) {
	
		int a = 10;
		int b = 20; 
		
		System.out.println("Original Numbers a = "+a+"  b= "+b);
		int c = a; //c=10
		a = b; // a=20;
		 b = c; //b = 10;
		 
		 System.out.println("After Swapping a = "+a+"  b= "+b);
		

	}

}
